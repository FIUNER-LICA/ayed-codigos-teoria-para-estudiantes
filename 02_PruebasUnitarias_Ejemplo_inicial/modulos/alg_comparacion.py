'''
Módulo con un algoritmo de comparación básico.
'''

def mayor(valor1, valor2):
    '''
    Función que compara dos valores y retorna el mayor.
    '''
    mas_grande = valor1
    if valor2 > valor1:
        mas_grande = valor2	
    return mas_grande


